clear;
close all;
clc;

projectRoot = fileparts(mfilename("fullpath"));
addpath(genpath(projectRoot));

trajectoryType = "quad";
Ts = 0.01;
segmentTime = 3;
repetitions = 1;
yawDesired = 0;

saveImage = false;

simConfig = SimulationPlanner( ...
    "TrajectoryType", trajectoryType, ...
    "Ts", Ts, ...
    "SegmentTime", segmentTime, ...
    "Repetitions", repetitions, ...
    "YawDesired", yawDesired, ...
    "IntegrationMethod", "euler", ...
    "InitialState", zeros(12,1), ...
    "ResetStateEachLap", false);

trajectory = GenerateTrajectory(simConfig);

t = simConfig.time.t;
r = trajectory.ref.r;
v = trajectory.ref.v;
a = trajectory.ref.a;
waypoints = trajectory.waypoints.base;

fprintf("\n--- Trajetoria de referencia ---\n");
fprintf("Tipo: %s\n", simConfig.trajectory.type);
fprintf("Waypoints: %d\n", simConfig.trajectory.numWaypointsPerLap);
fprintf("Segmentos: %d\n", simConfig.trajectory.numSegmentsPerLap);
fprintf("Tempo por segmento: %.2f s\n", simConfig.time.segmentTime);
fprintf("Tempo total: %.2f s\n", simConfig.time.tf);
fprintf("Amostras: %d\n", numel(t));

f1 = figure("Name", "Trajetoria de Referencia 3D", ...
    "NumberTitle", "off", ...
    "Color", "w", ...
    "InvertHardcopy", "off");

ax1 = axes("Parent", f1);
hold(ax1, "on");

plot3(ax1, r(1,:), r(2,:), r(3,:), "LineWidth", 2);
plot3(ax1, waypoints(:,1), waypoints(:,2), waypoints(:,3), "o", ...
    "MarkerSize", 5, ...
    "LineWidth", 1.2);
plot3(ax1, r(1,1), r(2,1), r(3,1), "s", ...
    "MarkerSize", 8, ...
    "LineWidth", 1.5);
plot3(ax1, r(1,end), r(2,end), r(3,end), "d", ...
    "MarkerSize", 8, ...
    "LineWidth", 1.5);

set(ax1, ...
    "Color", "w", ...
    "XColor", "k", ...
    "YColor", "k", ...
    "ZColor", "k", ...
    "GridColor", [0.65 0.65 0.65], ...
    "MinorGridColor", [0.80 0.80 0.80], ...
    "GridAlpha", 0.45, ...
    "MinorGridAlpha", 0.25);

grid(ax1, "on");
box(ax1, "on");
axis(ax1, "equal");

xlabel(ax1, "x [m]", "Color", "k");
ylabel(ax1, "y [m]", "Color", "k");
zlabel(ax1, "z [m]", "Color", "k");
title(ax1, "Trajetoria de referencia", "Color", "k");

lgd1 = legend(ax1, "Referencia", "Waypoints", "Inicio", "Fim", "Location", "best");
set(lgd1, ...
    "Color", "w", ...
    "TextColor", "k", ...
    "EdgeColor", "k");

view(ax1, 3);

f2 = figure("Name", "Posicao de Referencia", ...
    "NumberTitle", "off", ...
    "Color", "w", ...
    "InvertHardcopy", "off");

ax2 = axes("Parent", f2);
hold(ax2, "on");

plot(ax2, t, r(1,:), "LineWidth", 1.5);
plot(ax2, t, r(2,:), "LineWidth", 1.5);
plot(ax2, t, r(3,:), "LineWidth", 1.5);

set(ax2, ...
    "Color", "w", ...
    "XColor", "k", ...
    "YColor", "k", ...
    "GridColor", [0.65 0.65 0.65], ...
    "MinorGridColor", [0.80 0.80 0.80], ...
    "GridAlpha", 0.45, ...
    "MinorGridAlpha", 0.25);

grid(ax2, "on");
box(ax2, "on");

xlabel(ax2, "Tempo [s]", "Color", "k");
ylabel(ax2, "Posicao [m]", "Color", "k");
title(ax2, "Posicao desejada", "Color", "k");

lgd2 = legend(ax2, "x_d", "y_d", "z_d", "Location", "best");
set(lgd2, ...
    "Color", "w", ...
    "TextColor", "k", ...
    "EdgeColor", "k");

f3 = figure("Name", "Velocidade de Referencia", ...
    "NumberTitle", "off", ...
    "Color", "w", ...
    "InvertHardcopy", "off");

ax3 = axes("Parent", f3);
hold(ax3, "on");

plot(ax3, t, v(1,:), "LineWidth", 1.5);
plot(ax3, t, v(2,:), "LineWidth", 1.5);
plot(ax3, t, v(3,:), "LineWidth", 1.5);

set(ax3, ...
    "Color", "w", ...
    "XColor", "k", ...
    "YColor", "k", ...
    "GridColor", [0.65 0.65 0.65], ...
    "MinorGridColor", [0.80 0.80 0.80], ...
    "GridAlpha", 0.45, ...
    "MinorGridAlpha", 0.25);

grid(ax3, "on");
box(ax3, "on");

xlabel(ax3, "Tempo [s]", "Color", "k");
ylabel(ax3, "Velocidade [m/s]", "Color", "k");
title(ax3, "Velocidade desejada", "Color", "k");

lgd3 = legend(ax3, "vx_d", "vy_d", "vz_d", "Location", "best");
set(lgd3, ...
    "Color", "w", ...
    "TextColor", "k", ...
    "EdgeColor", "k");

f4 = figure("Name", "Aceleracao de Referencia", ...
    "NumberTitle", "off", ...
    "Color", "w", ...
    "InvertHardcopy", "off");

ax4 = axes("Parent", f4);
hold(ax4, "on");

plot(ax4, t, a(1,:), "LineWidth", 1.5);
plot(ax4, t, a(2,:), "LineWidth", 1.5);
plot(ax4, t, a(3,:), "LineWidth", 1.5);

set(ax4, ...
    "Color", "w", ...
    "XColor", "k", ...
    "YColor", "k", ...
    "GridColor", [0.65 0.65 0.65], ...
    "MinorGridColor", [0.80 0.80 0.80], ...
    "GridAlpha", 0.45, ...
    "MinorGridAlpha", 0.25);

grid(ax4, "on");
box(ax4, "on");

xlabel(ax4, "Tempo [s]", "Color", "k");
ylabel(ax4, "Aceleracao [m/s^2]", "Color", "k");
title(ax4, "Aceleracao desejada", "Color", "k");

lgd4 = legend(ax4, "ax_d", "ay_d", "az_d", "Location", "best");
set(lgd4, ...
    "Color", "w", ...
    "TextColor", "k", ...
    "EdgeColor", "k");

if saveImage
    exportgraphics(f1, "trajetoria_referencia_3d.png", ...
        "Resolution", 300, ...
        "BackgroundColor", "white");

    exportgraphics(f2, "posicao_referencia.png", ...
        "Resolution", 300, ...
        "BackgroundColor", "white");

    exportgraphics(f3, "velocidade_referencia.png", ...
        "Resolution", 300, ...
        "BackgroundColor", "white");

    exportgraphics(f4, "aceleracao_referencia.png", ...
        "Resolution", 300, ...
        "BackgroundColor", "white");
end